let currentLevel = "";
let currentQuestionIndex = 0;
let score = 0;

const welcomeContainer = document.getElementById("welcome-container");
const quizContainer = document.getElementById("quiz-container");
const levelSpan = document.getElementById("level");
const questionDiv = document.getElementById("question");
const optionsDiv = document.getElementById("options");
const submitBtn = document.getElementById("submit-btn");
const scoreSpan = document.getElementById("score");

const easyBtn = document.getElementById("easy-btn");
const mediumBtn = document.getElementById("medium-btn");
const hardBtn = document.getElementById("hard-btn");

easyBtn.addEventListener("click", () => startQuiz("Easy"));
mediumBtn.addEventListener("click", () => startQuiz("Medium"));
hardBtn.addEventListener("click", () => startQuiz("Hard"));
submitBtn.addEventListener("click", checkAnswer);

function startQuiz(level) {
    currentLevel = level;
    levelSpan.textContent = level;
    currentQuestionIndex = 0;
    score = 0;
    userAnswers.length = 0;
    updateScore();

    welcomeContainer.style.display = "none";
    quizContainer.style.display = "block";

    showQuestion();
}

function showQuestion() {
    const questions = getQuestionsForLevel(currentLevel);

    if (currentQuestionIndex < questions.length) {
        const currentQuestion = questions[currentQuestionIndex];
        questionDiv.textContent = currentQuestion.question;

        optionsDiv.innerHTML = "";

        currentQuestion.options.forEach((optionText, index) => {
            const optionLabel = document.createElement("label");
            const optionRadio = document.createElement("input");
            optionRadio.type = "radio";
            optionRadio.name = "answer";
            optionRadio.value = optionText;
            optionLabel.appendChild(optionRadio);
            optionLabel.appendChild(document.createTextNode(optionText));
            optionsDiv.appendChild(optionLabel);
        });
    } else {
        const questions = getQuestionsForLevel(currentLevel);
        const correctAnswers = questions.filter((question, index) => {
            return question.answer === userAnswers[index];
        }).length;
        const incorrectAnswers = questions.length - correctAnswers;

        questionDiv.textContent = "Level Complete!";
        optionsDiv.style.display = "none";
        submitBtn.style.display = "none";
        scoreSpan.textContent = `Your Score: ${score}`;
        const resultDiv = document.createElement("div");
        resultDiv.innerHTML = `Correct Answers: ${correctAnswers}<br>Incorrect Answers: ${incorrectAnswers}`;
        quizContainer.appendChild(resultDiv);

        const backButton = document.createElement("button");
        backButton.textContent = "Back to Main Menu";
        backButton.addEventListener("click", () => {
            quizContainer.style.display = "none";
            welcomeContainer.style.display = "block";
        });
        quizContainer.appendChild(backButton);
    }
}

const userAnswers = [];

function checkAnswer() {
    const questions = getQuestionsForLevel(currentLevel);
    const currentQuestion = questions[currentQuestionIndex];
    const selectedOption = document.querySelector('input[name="answer"]:checked');

    if (selectedOption) {
        const userAnswer = selectedOption.value;
        userAnswers.push(userAnswer);

        if (userAnswer === currentQuestion.answer) {
            score++;
        }

        currentQuestionIndex++;
        selectedOption.checked = false;

        showQuestion();
        updateScore();
    }
}

function updateScore() {
    scoreSpan.textContent = `Your Score: ${score}`;
}

function getQuestionsForLevel(level) {
    switch (level) {
        case "Easy":
            return easyQuestions;
        case "Medium":
            return mediumQuestions;
        case "Hard":
            return hardQuestions;
        default:
            return [];
    }
}
